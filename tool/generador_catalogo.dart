// Offline authoring library for the 15 bundled catalog levels (Ticket 31).
//
// This is the Flutter-free, deterministic core the `generar_niveles.dart`
// script orchestrates. It is split out from the script so it can be unit-tested
// directly (no `dart:io`, no asset bundle).
//
// What ticket 31 adds on top of the original tool:
//   • shape rotation — each authored level carries the shape mask ticket 23's
//     [RepertorioFormas] assigns to its index (level 1 → Cuadrado, 2 → Corazón,
//     …), so the rotation starts at Level 1 and is continuous into the endless
//     tail. The repertoire is *reused*, never re-declared here.
//   • raised difficulty bands — the grid floor is 7×7: levels 1–5 → 7×7,
//     6–10 → 8×8, 11–15 → 9×9 (PRD §12 req 2), tuned in one place ([perfilNivel]).
//   • sparse boards — absent positions (outside the shape) are simply omitted
//     from the serialized `cells`; there is no filler and no `type: "absent"`
//     marker. The runtime loader treats an omitted position as absent.

